#include "ctm_symtab.h"
#include "ctm_token.h"

#include <stdlib.h>
#include <string.h>

void
insert_symtab(CtmSymtab_t ** symtab, TokenType_t type, char * str)
{
    CtmSymtab_t * new = malloc(sizeof(CtmSymtab_t));
    new->str = malloc(strlen(str) * sizeof(char));
    strcpy(new->str, str);
    new->next = (*symtab);
    new->type = type;
    (*symtab) = new;
}

size_t
get_symbol_register(CtmSymtab_t * symtab, char * str)
{
    if (!symtab) return 0;
    if (strcmp(symtab->str, str) == 0) return symtab->reg;
    return get_symbol_register(symtab->next, str);
}

void
print_symtab(CtmSymtab_t * symtab)
{
    if (!symtab) return;
    printf("%zu: %s\n", symtab->reg, symtab->str);
    print_symtab(symtab->next);
}